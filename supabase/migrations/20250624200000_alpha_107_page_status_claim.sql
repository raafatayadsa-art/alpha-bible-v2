-- ---------------------------------------------------------------------------
-- Submit church claim → pending_claim + platform approval queue
-- ---------------------------------------------------------------------------
create or replace function public.submit_church_claim(
  p_church_id bigint,
  p_note text default null
)
returns json
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user uuid := auth.uid();
  v_church record;
  v_claim_id uuid;
  v_approval_id uuid;
  v_request_no text;
begin
  if v_user is null then
    raise exception 'not_authenticated';
  end if;

  select id, church_name, page_status, is_active
  into v_church
  from public.churches
  where id = p_church_id and is_active = true;

  if not found then
    raise exception 'church_not_found';
  end if;

  if v_church.page_status = 'verified' then
    raise exception 'already_verified';
  end if;

  if v_church.page_status = 'pending_claim' then
    raise exception 'claim_pending';
  end if;

  if exists (
    select 1 from public.church_claim_requests
    where church_id = p_church_id and status = 'pending'
  ) then
    raise exception 'claim_pending';
  end if;

  insert into public.church_claim_requests (church_id, user_id, note)
  values (p_church_id, v_user, nullif(btrim(p_note), ''))
  returning id into v_claim_id;

  v_request_no := 'CC-' || to_char(now(), 'YYYY') || '-' || lpad(floor(random() * 10000)::text, 4, '0');

  insert into public.platform_approvals (
    request_no, kind, title, kind_label, status, priority, payload
  )
  values (
    v_request_no,
    'church_claim',
    'طلب استلام — ' || v_church.church_name,
    'استلام كنيسة',
    'pending',
    'normal',
    jsonb_build_object(
      'churchId', p_church_id,
      'claimId', v_claim_id,
      'submittedBy', v_user::text,
      'note', coalesce(p_note, '')
    )
  )
  returning id into v_approval_id;

  update public.church_claim_requests
  set approval_id = v_approval_id, updated_at = now()
  where id = v_claim_id;

  update public.churches
  set page_status = 'pending_claim', updated_at = now()
  where id = p_church_id;

  return json_build_object(
    'ok', true,
    'claimId', v_claim_id,
    'approvalId', v_approval_id,
    'pageStatus', 'pending_claim'
  );
end;
$$;

grant execute on function public.submit_church_claim(bigint, text) to authenticated;

create or replace function public.get_church_claim_status(p_church_id bigint)
returns json
language sql
stable
security definer
set search_path = public
as $$
  select json_build_object(
    'pageStatus', c.page_status,
    'hasPendingClaim', exists (
      select 1 from public.church_claim_requests r
      where r.church_id = p_church_id and r.status = 'pending'
    ),
    'userHasPendingClaim', exists (
      select 1 from public.church_claim_requests r
      where r.church_id = p_church_id
        and r.status = 'pending'
        and r.user_id = auth.uid()
    )
  )
  from public.churches c
  where c.id = p_church_id;
$$;

grant execute on function public.get_church_claim_status(bigint) to anon, authenticated;
