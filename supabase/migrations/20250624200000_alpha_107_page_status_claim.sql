-- ALPHA-107 Phase 0: unified page lifecycle (churches + monasteries + claims)

do $$
begin
  if not exists (select 1 from pg_type where typname = 'place_page_status') then
    create type public.place_page_status as enum (
      'inactive',
      'pending_claim',
      'verified',
      'suspended'
    );
  end if;
end $$;

-- Legacy monasteries seed table (pre-107) — add page lifecycle column
alter table public.monasteries
  add column if not exists page_status public.place_page_status not null default 'inactive';

alter table public.churches
  add column if not exists page_status public.place_page_status;

update public.churches
set page_status = case
  when coalesce(is_active, true) = false then 'suspended'::public.place_page_status
  when coalesce(is_verified, false) or coalesce(location_verified, false) then 'verified'::public.place_page_status
  else 'inactive'::public.place_page_status
end
where page_status is null;

alter table public.churches
  alter column page_status set default 'inactive'::public.place_page_status;

create index if not exists churches_page_status_idx on public.churches (page_status);

-- ---------------------------------------------------------------------------
-- Church claim requests
-- ---------------------------------------------------------------------------
create table if not exists public.church_claim_requests (
  id uuid primary key default gen_random_uuid(),
  church_id bigint not null references public.churches (id) on delete cascade,
  user_id uuid not null references auth.users (id) on delete cascade,
  status text not null default 'pending' check (status in ('pending', 'approved', 'rejected', 'cancelled')),
  note text,
  approval_id uuid references public.platform_approvals (id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists church_claim_requests_church_idx on public.church_claim_requests (church_id);
create index if not exists church_claim_requests_user_idx on public.church_claim_requests (user_id);
create unique index if not exists church_claim_requests_one_pending_per_church
  on public.church_claim_requests (church_id)
  where status = 'pending';

-- ---------------------------------------------------------------------------
