  id bigint generated by default as identity primary key,
  monastery_name text not null,
  english_name text,
  city text,
  governorate text,
  country text default 'Egypt',
  formatted_address text,
  description text,
  cover_image_url text,
  hero_image text,
  latitude numeric,
  longitude numeric,
  phone text,
  email text,
  website_url text,
