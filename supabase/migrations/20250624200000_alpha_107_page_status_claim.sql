create table if not exists public.monastery_claim_requests (
  id uuid primary key default gen_random_uuid(),
  monastery_id bigint not null references public.monasteries (id) on delete cascade,
  user_id uuid not null references auth.users (id) on delete cascade,
  status text not null default 'pending' check (status in ('pending', 'approved', 'rejected', 'cancelled')),
  note text,
  approval_id uuid references public.platform_approvals (id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create unique index if not exists monastery_claim_requests_one_pending_per_monastery
  on public.monastery_claim_requests (monastery_id)
  where status = 'pending';

