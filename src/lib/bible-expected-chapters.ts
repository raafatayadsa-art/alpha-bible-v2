export function chapterCountStatus(book: string, actual: number): "ok" | "low" | "high" | "unknown" {
  const expected = expectedChapterCount(book);
  if (expected == null) return "unknown";
  if (actual === expected) return "ok";
  return actual < expected ? "low" : "high";
}
